'''
Object Oriented Analysis and Design is a way of thinking about a system as a collection of interacting objects, each with it's own responsibilities. 

The steps typically include:

Identify key objects (e.g., User, Order, Product)

Define relationships between them (e.g., associations, inheritance)

Design interfaces for how they interact (e.g., methods like placeOrder())

Translate the design into code using an object-oriented language like Java, C++, or Python

To document these ideas clearly, we use UML diagrams (like class diagrams, sequence diagrams, etc.)
'''