'''
A Use Case Diagram shows what the system does from the user's point of view. It mainly focuses on functional behavior

Actor → a user or system that interacts with your application (drawn as a stick figure)

Use Case → a specific task or function the system performs (drawn as an oval)

System Boundary → a box that wraps around all use cases (defines the system's scope)

Relationships:
Include → one use case always includes another (like a function call)
Example: “Place Order” includes “Validate Payment”

Extend → adds optional or conditional steps to another use case
Example: “Track Order” may extend “View Order”
'''